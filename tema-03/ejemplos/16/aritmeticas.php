<?php

    /*
        Funciones aritméticas
    */

    function sumar (int $var1, int $var2){
        return $var1 + $var2;
    }

    function restar (int $var1, int $var2){
        return $var1 - $var2;
    }

    function dividir (int $var1, int $var2){
        return $var1 / $var2;
    }
    
    function multiplicar (int $var1, int $var2){
        return $var1 * $var2;
    }


?>